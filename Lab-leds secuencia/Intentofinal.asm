LIST P=16F877A
RADIX HEX	

W EQU 0X00
ESTADO 	EQU 0X03
TRISB  	EQU 0X86
PUERTAB EQU 0X06
CONTA2	EQU 0X22
CONTA1	EQU	0X21
CONTA0	EQU	0X20

ORG 0X00
BSF 	ESTADO, 5
CLRF	TRISB
BCF 	ESTADO, 5
CLRF	PUERTAB

CICLO	
	MOVLW B'10000000'
	MOVWF PUERTAB
	CALL  RETARDO
	MOVLW B'01000000'
	MOVWF PUERTAB
	CALL  RETARDO
	MOVLW B'00100000'
	MOVWF PUERTAB
	CALL  RETARDO
	MOVLW B'00010000'
	MOVWF PUERTAB
	CALL  RETARDO
	MOVLW B'00001000'
	MOVWF PUERTAB
	CALL  RETARDO
	MOVLW B'00000100'
	MOVWF PUERTAB
	CALL  RETARDO
	MOVLW B'00000010'
	MOVWF PUERTAB
	CALL  RETARDO
	MOVLW B'00000001'
	MOVWF PUERTAB
	CALL  RETARDO
GOTO	CICLO
;Subrutinas de retardo de 1 Segundo
RETARDO	MOVLW	0X21
		MOVWF	CONTA2
RETA2	MOVLW	0X64
		MOVWF	CONTA1
RETA1	MOVLW	0X64
		MOVWF	CONTA0
RETA0	DECFSZ 	CONTA0,1
		GOTO	RETA0
		DECFSZ 	CONTA1,1
		GOTO	RETA1
		DECFSZ 	CONTA2,1
		GOTO	RETA2
		RETURN

END